# len()
print('python length:', len('python'), '\n')

# max() min()
# number
print('max(40,20,30,50,10) :', max(40,20,30,50,10), '\n')
print('min(40,20,30,50,10) :', min(40,20,30,50,10), '\n')
# string
print('max(abcdefg) :', max('abcdefg'), '\n')
print('min(abcdefg) :', min('abcdefg'), '\n')

# sorted()
print('sort (I am a engineer) :', sorted('I am a engineer'))
print('sort [40,20,30,50,10] :', sorted([40,20,30,50,10]))

# type()
var1 = 'abc'
print('var1 type: ', type(var1))

var2 = '123'
print('var2 type: ', type(var2))